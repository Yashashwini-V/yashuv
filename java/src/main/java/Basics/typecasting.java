package Basics;

public class typecasting {

	public static void main(String[] args) {
		char a = 58;
		System.out.println(a);
		
		 int b=a;
		System.out.println(b);
		 
		int c= (char) b;
		System.out.println(c);
		
		char h='y';
		int i= (char)h;
		System.out.println(i);
		
		int o = 97;
		char j = (char)o;
		System.out.println(j);
		

	}

}
